package dev.pastel.pastelboard.bluetooth

object HidProtocol {
    const val KEYBOARD_REPORT_ID = 1
    const val POINTER_REPORT_ID = 2
    const val KEYBOARD_REPORT_SIZE = 8
    const val POINTER_REPORT_SIZE = 4

    val descriptor: ByteArray
        get() = HID_DESCRIPTOR.copyOf()

    val descriptorReportIds: Set<Int> by lazy {
        HID_DESCRIPTOR.mapIndexedNotNull { index, value ->
            if ((value.toInt() and 0xFF) == 0x85 && index + 1 < HID_DESCRIPTOR.size) {
                HID_DESCRIPTOR[index + 1].toInt() and 0xFF
            } else {
                null
            }
        }.toSet()
    }

    private val HID_DESCRIPTOR = byteArrayOf(
        0x05, 0x01,
        0x09, 0x06,
        0xA1.toByte(), 0x01,
        0x85.toByte(), KEYBOARD_REPORT_ID.toByte(),
        0x05, 0x07,
        0x19, 0xE0.toByte(),
        0x29, 0xE7.toByte(),
        0x15, 0x00,
        0x25, 0x01,
        0x75, 0x01,
        0x95.toByte(), 0x08,
        0x81.toByte(), 0x02,
        0x95.toByte(), 0x01,
        0x75, 0x08,
        0x81.toByte(), 0x01,
        0x95.toByte(), 0x06,
        0x75, 0x08,
        0x15, 0x00,
        0x25, 0x65,
        0x05, 0x07,
        0x19, 0x00,
        0x29, 0x65,
        0x81.toByte(), 0x00,
        0xC0.toByte(),
        0x05, 0x01,
        0x09, 0x02,
        0xA1.toByte(), 0x01,
        0x85.toByte(), POINTER_REPORT_ID.toByte(),
        0x09, 0x01,
        0xA1.toByte(), 0x00,
        0x05, 0x09,
        0x19, 0x01,
        0x29, 0x03,
        0x15, 0x00,
        0x25, 0x01,
        0x95.toByte(), 0x03,
        0x75, 0x01,
        0x81.toByte(), 0x02,
        0x95.toByte(), 0x01,
        0x75, 0x05,
        0x81.toByte(), 0x01,
        0x05, 0x01,
        0x09, 0x30,
        0x09, 0x31,
        0x09, 0x38,
        0x15, 0x81.toByte(),
        0x25, 0x7F,
        0x75, 0x08,
        0x95.toByte(), 0x03,
        0x81.toByte(), 0x06,
        0xC0.toByte(),
        0xC0.toByte(),
    )
}
